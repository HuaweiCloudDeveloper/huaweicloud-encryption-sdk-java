package com.huaweicloud.encryptionsdk.exception;

public class CipherDataException extends HuaweicloudException {

    public CipherDataException() {
        super();
    }

    public CipherDataException(final String message) {
        super(message);
    }

    public CipherDataException(final Throwable cause) {
        super(cause);
    }

    public CipherDataException(final String message, final Throwable cause) {
        super(message, cause);
    }
}

package com.huaweicloud.encryptionsdk.exception;

public class EncryptException extends HuaweicloudException {
    public EncryptException() {
        super();
    }

    public EncryptException(final String message) {
        super(message);
    }

    public EncryptException(final Throwable cause) {
        super(cause);
    }

    public EncryptException(final String message, final Throwable cause) {
        super(message, cause);
    }
}
